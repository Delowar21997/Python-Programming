# Example of using underscores in numeric literals for better readability
population = 7_900_000_000  # world population
print(f"World population: {population}")
# Age of the universe in years
universe_age = 14_000_000_000  # in years
print(f"The age of the universe is approximately {universe_age} years.")
# Approximation of Pi with underscores for clarity
pi_approx = 3.14159_26535_8979323846
print(f"Approximation of Pi: {pi_approx}")
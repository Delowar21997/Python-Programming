# Boolean expression evaluation
print(1 > 2)  # False
print(3 < 4)  # True
print(5 == 5)  # True
print(6 != 7)  # True
print(8 >= 8)  # True
print(9 <= 10)  # True
print(11 > 12)  # False
print(13 < 14)  # True
print(15 == 16)  # False
print(17 != 18)  # True
print(19 >= 20)  # False
print(21 <= 22)  # True
car = "Honda"
print(f"The value of car is: \033[1;92m'{car}'\033[0m\n")
print(car == 'Toyota') # False
print(car != 'Toyota') # True
print(car == 'Honda') # True
print(car != 'Honda') # False
print(car == 'honda') # False (case-sensitive)
print(car != 'honda') # True (case-sensitive)
print(car == 'HONDA') # False (case-sensitive)
print(car != 'HONDA') # True (case-sensitive)
print(car == 'Honda') # True
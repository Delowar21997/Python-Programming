# float number operations
a = 0.1
b = 0.2
# Addition
sum_result = a + b
# Subtraction
diff_result = a - b
# Multiplication
prod_result = a * b
# Division
div_result = a / b
# Remainder
reminders = a % b
# Exponentiation
exp_result = a ** b
print("Sum:", sum_result)
print("Difference:", diff_result)
print("Product:", prod_result)
print("Division:", div_result)
print("Remainder:", reminders)
print("Exponentiation:", exp_result)
# sorted() function returns a new sorted list from the elements of any iterable.
my_list = ['banana', 'apple', 'cherry', 'date']
print("Original list:", my_list)
# Sorting the list in ascending order
print("Sorted list in ascending order:", sorted(my_list))
# Sorting the list in descending order
print("Sorted list in descending order:", sorted(my_list, reverse=True))
print("Original list after sorting:", my_list)  # Original list remains unchanged
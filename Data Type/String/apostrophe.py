# Apostrophe in Strings
# Using double quotes to include an apostrophe in a string
# Defining strings with apostrophes
# Using single quotes requires escaping the apostrophe
# Using double quotes avoids the need for escaping
text = "It's a beautiful day!"
text1 = '"Its a beautiful day!"'
text2 = 'It\'s a beautiful day!'

print(text)
print(text1)
print(text2)
